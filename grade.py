Percentage = float(input("ENTER YOUR PERCENTAGE = "))

if Percentage >= 79:
    print("Congrats , your Grade is A+")
elif Percentage >= 69:
    print("Congrats , your Grade is A")
elif Percentage >= 59:
    print("Congrats , your Grade is B")
elif Percentage >= 49:
    print("your Grade is C")
elif Percentage >= 39:
    print("your Grade is D")
else:
    print("your Grade is F")
